    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        
        String mail = txtMail.getText().trim();
        String sifre = txtSifre.getText().trim();
        
        String selectquery = "select * from admin_bilgiler where amail = '"+mail+"' and asifre = '"+sifre+"' ";
        
        try {
            ResultSet rs = new DB().baglan().executeQuery(selectquery);
            if(rs.next()){
                
                String yaz = rs.getString("aadi") + " " + rs.getString("asoyadi");
                JOptionPane.showMessageDialog(rootPane, "Hoş Geldiniz Sn. " + yaz );
                
                Siparis_Kayit sk = new Siparis_Kayit();
                sk.setVisible(true);
                dispose();
            
            }else{
                JOptionPane.showMessageDialog(rootPane, "Kullanıcı adı veya şifre hatalı");
            }
            
            
        } catch (Exception e) {
            System.err.println("kullanıcı girişi kontrol hatası : " + e);
        }
        
      
    }//GEN-LAST:event_jButton1ActionPerformed

